#string methods are isalpha,is_digit,is_alam(alphanumeric),
# is_upper,is_lower,check withlower,converting to uppercase,
# title case,swap case,
str=input()
print(str.upper())
print(str.lower())
print(str.capitalize())#first letter capital only first word
print(str.title())#all words first letter capital
print(str.swapcase())#upper to lower and lower to upper
print(str.strip())# remove space
print(str.replace('a','z'))
print(str.split())
print(str.split('a'))

